executor = ThreadPoolExecutor(max_workers=20)
stats = defaultdict(int)


def get_file_names_for_date(date):
    for format_index in range(0, 8):
        url = urls_formats['index'][format_index].format(date=date)
        response = requests.get(url)
        if response.status_code == 200:
            break
    else:
        stats['no-index'] += 1
        return [], 0

    file_names = []
    for line in response.content.decode('utf-8').split('\n'):
        if line.startswith('#') or not line.strip():
            continue

        file_names.append(line.strip())

    return file_names, format_index


def get_size_of_part(url):
    response = requests.head(url)
    if response.status_code == 404:
        stats[404] += 1
        return 0

    if response.status_code != 200:
        stats[response.status_code] += 1
        return 0

    if 'content-length' not in response.headers:
        stats['no-length'] += 1
        return 0

    return response.headers['content-length']


def compute_size_of_date(date, index_file_names, format_index):
    futures = []
    print('creating all tasks')
    for file_name in index_file_names:
        url = urls_formats['parts'][format_index].format(date=date, file_name=file_name)
        futures.append(executor.submit(get_size_of_part, url))

    print('waiting for results')
    total = 0
    for future in futures:
        total += int(future.result())
        print(total / 1024 / 1024, 'MB', end='\r')

    print()
    return total


def get_size_for_url(url):
    url_info = urlparse(url)
    date = '/'.join(url_info.path.split('/')[-3:])

    index_file_names, format_index = get_file_names_for_date(date)
    if not index_file_names:
        return 0

    return compute_size_of_date(date, index_file_names, format_index)


